#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>

int main() {
    printf("Executing 'ls -Rl' using execv...\n");

    char *args[] = {"ls", "-Rl", NULL};

    execv("/bin/ls", args);

    perror("execv failed");
    return 1;
}

